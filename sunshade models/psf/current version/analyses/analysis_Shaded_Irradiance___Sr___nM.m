
function    [shaded_irradiances] = ...
			........................................................................................................................................................ 
            analysis_Shaded_Irradiance___Sr___nM(time_UTC)



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Import all analysis variables.                                                                                                                          %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Import all analysis variables.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


[model_parameters, radii, luminosity_S, spectral_data, change_of_bases, origins, partitions] = import_Analysis_Variables___ASr___6SrS({'planet', 'shade'}, time_UTC);


%%&%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Prepare partitions. 																								         				            %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Transform initial Shade partition vectors (positions, normals) into the common coordinate system, relocate the origin of its position vectors accordingly,  
%   and normalize its normal vectors.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


partitions.shade.initial   = partition_Transform_Shift_Normalize___SrMC___Sr(partitions.shade.heliogyros.initial, change_of_bases.shade, origins.shade);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Sort the shade position and normal vectors by the coordinate values of their positions (x > y > z).  
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


[~, indexes_sorted]                     = sortrows(partitions.shade.initial.positions',[1 2 3]);
indexes_sorted                          = indexes_sorted';

partitions.shade.initial.positions      = partitions.shade.initial.positions(:,indexes_sorted);
partitions.shade.initial.normal_vectors = partitions.shade.initial.normal_vectors(:,indexes_sorted);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Scale the Planet partition's normal vectors by the radius of the planet (to get positions), and perform the appropriate change of basis and origin shift so 
%	they're represented in the common coordinate system.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


partitions.positions.planet             = partition_Scale_Transform_Shift___2MCS___Sr(partitions.positions.planet, change_of_bases.planet, origins.planet);


%%&%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Analyze shaded irradiances.                                                                                                                             %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Create variable to store shaded irradiances.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


shaded_irradiances = zeros([model_parameters.planet.dimensions.actual spectral_data.number_of_bands]);


%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%
%%%%	For each partitioned planet subsurface: 
%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%


for i = 1:size(partitions.positions.planet, 2)
for j = 1:size(partitions.positions.planet, 3)


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Identify position and unit normal vector associated with the planet subsurface.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


	position_P              = partitions.positions.planet(:,i,j);
	unit_normal_vector_P    = partitions.unit_normal_vectors.planet(:,i,j);
	unit_normal_vectors_S   = partitions.shade.initial.normal_vectors;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Calculate the vectors associated with the propagation of the Planet-Shade axes to the surface off the Star.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


	[vectors]               = propagate_Vectors_To_Sphere___M2C___Sr(partitions.shade.initial.positions, position_P, origins.star, radii.star);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Eliminate normal vectors for Planet-Shade axes that didn't intersect the Star.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


	unit_normal_vectors_S   = unit_normal_vectors_S(:, vectors.that_intersect_sphere);
	

%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Project the areas of the shades onto the Planet-Shade-Star axes    
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


	projected_shade_areas   = model_parameters.shade.heliogyro.area_effective * vectors.unit.p.S .* unit_normal_vectors_S;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Project the resulting shaded areas geometrically (like a cone) to the points on the Star surface.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%

	
	projected_shade_areas   = projected_shade_areas .* (vectors.norms.p.S ./ vectors.norms.p.H).^2;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Project the resulting shaded areas onto the normal vectors on the Star surface. This gives the shaded areas on the sun.   
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


	projected_shade_areas   = projected_shade_areas .* max(0, -vectors.unit_normal.S .* vectors.unit.p.S);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Calculate the total luminosity coming from the shaded areas on the sun surface.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


	projected_shade_areas   = (luminosity_S / (4*pi*radii.star^2)) * projected_shade_areas;


%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%
%%%%	For each spectral band:
%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%


	for k = 1:spectral_data.number_of_bands


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Identify the projection coefficients from the Star-Planet axis to the Star and Planet subsurfaces.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


		projections_SPxS               = max(0, -vectors.unit_normal.S .* vectors.unit.p.S);
		projections_SPxS               = sum(projections_SPxS,1);

		projections_PSxP               = max(0, unit_normal_vector_P .* vectors.unit.p.S);
		projections_PSxP               = sum(projections_PSxP, 1);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Use one factor of the Star's projections to account for surface alignment, and another expanded in a power series to account for limb darkening.       
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


		limb_darkened_projections_SPxS = projections_SPxS .* power_Series_Of_Matrix___MC___M (projections_SPxS, spectral_data.coefficients(k,:));


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Calculate the spectral solar irradiance incident on each partitioned Planet subsurface from each partitioned Star subsurface.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


		spectral_shade_areas           = spectral_data.irradiances(k) * limb_darkened_projections_SPxS .* projected_shade_areas .* vectors.norms.p.S.^(-2);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Project shaded spectral irradiances onto the planet subsurface.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


		spectral_shade_areas           = spectral_shade_areas .* projections_PSxP;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Sum over all shaded spectral irradiances for the planet subsurface.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


		shaded_irradiances(i,j,k)      = sum(spectral_shade_areas,'all');


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Convert irradiance units from km^-2 to m^-2.            
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


		shaded_irradiances(i,j,k)      = shaded_irradiances(i,j,k) * (10^(-3))^2;


%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%
%%%%	End iterations over spectral bands. 
%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%

	end

%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%
%%%%	End iterations over planet subsurfaces. 
%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%


end
end


