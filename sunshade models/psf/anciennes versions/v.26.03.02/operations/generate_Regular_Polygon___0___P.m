%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	<Description>     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


n_vertices_outer = 10000;
n_vertices_inner = 10000;


radius_outer     = 10;
radius_inner     = 5;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	<Description>     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


angle_partitions_outer = [0 : (2*pi/n_vertices_outer) : 2*pi];
angle_partitions_inner = [0 : (2*pi/n_vertices_inner) : 2*pi];


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	<Description>     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


if angle_partitions_outer(1,end) == 2*pi

	angle_partitions_outer = angle_partitions_outer(1,1:end-1);

end


if angle_partitions_inner(1,end) == 2*pi

	angle_partitions_inner = angle_partitions_inner(1,1:end-1);

end
	

%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	<Description>     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


x_coordinates_outer = radius_outer * cos(angle_partitions_outer);
y_coordinates_outer = radius_outer * sin(angle_partitions_outer);

x_coordinates_inner = radius_inner * cos(angle_partitions_inner);
y_coordinates_inner = radius_inner * sin(angle_partitions_inner);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	<Description>     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


polygon = polyshape({x_coordinates_outer,x_coordinates_inner},{y_coordinates_outer,y_coordinates_inner});


