
function    [unit_normal_vectors_S, unit_vectors_PS, vector_norms_PS, vector_norms_PH] = ...
			........................................................................................................................................................
            shade_Sun_Vectors___M2C___3M (vectors_H, vector_P, origin_S, radius_S)


%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	<Description>                                                                                                                                           %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Calculate the relevant vectors for input into the quadratic equation.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


vectors_PH                      = vectors_H - vector_P;
vector_PS                       = vector_P - origin_S; 


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Identify the quadratic equation coefficients.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


a                               = vecnorm(vectors_PH).^2;
b                               = sum(2*vectors_PH .* vector_PS, 1);
c                               = norm(vector_PS)^2 - radius_S^2;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Identify quadratic equation solutions.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


t_1                             = (-b + (b.^2 - 4 .* a .* c).^(.5)) ./ (2*a);
t_2                             = (-b - (b.^2 - 4 .* a .* c).^(.5)) ./ (2*a);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Set imaginary and negative solutions to NaN.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


t_1(imag(t_1) ~= 0 | t_1 <= 0 ) = NaN;
t_2(imag(t_2) ~= 0 | t_2 <= 0 ) = NaN;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Take the minimim of both positive solutions (to avoid solutions that intersect the sun twice). 
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


t_1                             = min(t_1,t_2);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	<Description>     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


vector_norms_PH                 = vecnorm(vectors_PH, 2, 1);

vector_norms_PS                 = t_1 .* vector_norms_PH;
unit_vectors_PS                 = vectors_PH ./ vector_norms_PH;
unit_normal_vectors_S           = t_1 .* vectors_PH - origin_S;
unit_normal_vectors_S           = unit_normal_vectors_S ./ vecnorm(unit_normal_vectors_S, 2, 1);


