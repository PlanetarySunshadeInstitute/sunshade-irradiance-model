
function    [shaded_irradiances] = ...
			........................................................................................................................................................ 
            analysis_Shaded_Irradiance___0___nM



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Start the program stopwatch and import all analysis variables.                                                                                          %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Start program stop-watch.    
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


tic


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Import all analysis variables.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


[model_parameters, radii, luminosity_S, spectral_data, change_of_bases, origins, partitions] = import_Analysis_Variables___A___6SrS({'planet', 'shade'});


%%&%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Calculate: intermediate irradiance result (everything but the geometric projection onto the Star's surface and limb darkening).				            %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Transform initial Shade partition vectors (positions, normals) into the common coordinate system, relocate the origin of its position vectors accordingly,  
%   and normalize its normal vectors.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


partitions.shade.initial   = partition_Transform_Shift_Normalize___SrMC___Sr(partitions.shade.heliogyros.initial, change_of_bases.shade, origins.shade);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Sort the shade position and normal vectors by the coordinate values of their positions (x > y > z).  
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


[~, indexes_sorted]                     = sortrows(partitions.shade.initial.positions',[1 2 3]);
indexes_sorted                          = indexes_sorted';

partitions.shade.initial.positions      = partitions.shade.initial.positions(:,indexes_sorted);
partitions.shade.initial.normal_vectors = partitions.shade.initial.normal_vectors(:,indexes_sorted);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Scale the Planet partition's normal vectors by the radius of the planet (to get positions), and perform the appropriate change of basis and origin shift so 
%	they're represented in the common coordinate system.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


partitions.positions.planet = partition_Scale_Transform_Shift___2MCS___Sr(partitions.unit_normal_vectors.planet, change_of_bases.planet, origins.planet, radii.planet);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Create variable to store shaded irradiances.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


shaded_irradiances = zeros(model_parameters.planet.dimensions.actual);


%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%
%%%%	<Description>
%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%


for i = 1:size(partitions.positions.planet, 2)

for j = 1:size(partitions.positions.planet, 3)


vector_P = partitions.positions.planet(:,i,j);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	<Description>     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


[unit_normal_vectors_S, unit_vectors_PS, vector_norms_PS, vector_norms_PH] = shade_Sun_Vectors___M2C___3M (partitions.shade.initial.positions, vector_P, origins.star, radii.star);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Project shaded areas to sun.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


projected_shade_areas = model_parameters.shade.heliogyro.area * unit_vectors_PS .* partitions.shade.initial.normal_vectors .* vector_norms_PS ./ vector_norms_PH;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Project shaded areas to sun surface.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


projected_shade_areas = projected_shade_areas .* max(0, -unit_normal_vectors_S .* unit_vectors_PS);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Calculate the luminosity coming from the projected area.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


projected_shade_areas = (luminosity_S / (4*pi*radii.star^2)) * projected_shade_areas;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Project shaded irradiance back to planet subsurface.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


projected_shade_areas = projected_shade_areas .* max(0, -unit_normal_vectors_S .* unit_vectors_PS) .* vector_norms_PS.^(-2); 


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Project shaded irradiance onto planet subsurface.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


projected_shade_areas = projected_shade_areas .* max(0, partitions.unit_normal_vectors.planet(:,i,j) .* unit_vectors_PS);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Sum over all shaded irradiances for the subsurface.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%

projected_shade_areas(isnan(projected_shade_areas)) = 0;

shaded_irradiances(i,j) = sum(projected_shade_areas,'all');


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Convert irradiance units from km^-2 to m^-2.            
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


shaded_irradiances(i,j) = shaded_irradiances(i,j) * (10^(-3))^2;


%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%
%%%%	<Description>
%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%

end
end







%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Stop Watch: End Timing Program                                                                                                                          %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	End program stop-watch.  
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


toc


