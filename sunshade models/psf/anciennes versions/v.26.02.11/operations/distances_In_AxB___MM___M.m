
function    [distances_in_AxB] = ...
			........................................................................................................................................................
            distances_In_AxB___MM___M (set_A, set_B, basis_A, basis_B, origin_A, origin_B)



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Assume the last dimension of the sets' matrix representations stores vectors, and the remaining serve as indices.                                       %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	List the dimensions present in the sets.   
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


dimensions_set          = [1:ndims(set_A)];


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Identify the dimension where vectors are stored.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


dimensions_vector       = size(set_A, dimensions_set(1,end));


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Assume all other set dimensions are indices, and make the output's first dimensions correspond to the indices of set A, and the remaining to those of set B.       
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


dimensions_output       = [size(set_A, dimensions_set(1:end-1)) size(set_B, dimensions_set(1:end-1))];


%%&%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Calculate the distances between all vector pairs in {AxB}, accouunting for any differences in the origins/bases used in their matrix representations.   %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Cyclically permute the dimensions of the input sets, so the vector information is now stored in the first dimension.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


dimensions_set_permuted = circshift(dimensions_set,1);


set_A                   = permute(set_A, dimensions_set_permuted);
set_B                   = permute(set_B, dimensions_set_permuted);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Reshape the input sets into 2D arrays, such that each column corresponds to a particular vector.   
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


set_A                   = reshape(set_A, dimensions_vector, []);
set_B                   = reshape(set_B, dimensions_vector, []);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Transform the coordinates of set A and set B vectors (assuming their bases are represented with respect to the same coordinate system).    
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


set_A                   = basis_A * set_A;
set_B                   = basis_B * set_B;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Swwap the 2nd and 3rd dimensions of set B's representation, such that its column vectors now extend along the latter. Then replicate this 1st and 3rd 
%	dimension array along the 2nd dimension  along the column to mimick the dimensions of A (so that distances may be taken between each pair in AxB).        
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


set_B                   = permute(set_B,[1 3 2]);
set_B                   = repmat(set_B, [1 length(set_A(1,:)) 1]);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Find the differences between each vector pair in AxB, accounting for differences in the origins of the coordinate systems of their matrix representations:
%
%		- Assume that origin_A and origin_B are represented with respect to the same coordinate system.
%		- Take the Euclidian Norm (L2) along the 1st dimension.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


distances_in_AxB        = vecnorm(set_A - ((origin_B - origin_A) + set_B), 2, 1);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Reshape the output matrix so that its first dimensions correspond to the indices of set A and the last to those of set B.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


distances_in_AxB        = reshape(distances_in_AxB, dimensions_output);


