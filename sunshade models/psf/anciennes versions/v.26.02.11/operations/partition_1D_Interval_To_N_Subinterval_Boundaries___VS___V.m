function    [partition_subinterval_boundaries] = ...
			........................................................................................................................................................
            partition_1D_Interval_To_N_Subinterval_Boundaries___VS___V (bounds_lower_and_upper, n)



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Divide the input interval into n equal parts, then return a row vector containing the boundaires of each subinterval.                                   %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Identify the input lower and upper bounds, and Calculate the magnitude of the resulting interval. 
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


bound_lower                                   = bounds_lower_and_upper(1,1);
bound_upper                                   = bounds_lower_and_upper(1,2);


interval_magnitude                            = bound_upper - bound_lower;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Calculate the magnitude of the subintervals created when dividing the input interval into n equal parts.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


subinterval_magnitude                         = interval_magnitude / n;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Create a vector containing the boundaries of each subinterval.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


partition_subinterval_boundaries              = bound_lower : subinterval_magnitude : bound_upper;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	If the upper bound was excluded (meaning rounding error caused its exclusion in the construction above), add it to the list of partitioned boundaries.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


if partition_subinterval_boundaries(1,end) ~= bound_upper


	partition_subinterval_boundaries(1,end+1) = bound_upper;


end


