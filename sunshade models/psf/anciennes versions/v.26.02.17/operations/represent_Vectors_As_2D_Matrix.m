
function    [set_of_vectors, indice_dimension_magnitudes ] = ...
			........................................................................................................................................................
            represent_Vectors_As_2D_Matrix (set_of_vectors, basis)


%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Represent the input set of vectors as a 2D matrix, and perform a change of coordinates based on the input basis assosicated with the input set.         %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Assuming the first dimension of the set's matrix representations stores vector information, and the remaining serve as indices, list the index dimensions.  
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


indice_dimensions           = [2:ndims(set_of_vectors)];


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Find the dimensions of the set's vectors.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


vector_dimensions           = size(set_of_vectors, 1);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Define the magnitude of the output's dimensions to correspond to those of set A's indices (to enable reformatting downstream).       
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


indice_dimension_magnitudes = [size(set_of_vectors, indice_dimensions)];


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Reshape the matrix representations of the input set into a 2D array, such that vectors are expressed along the first dimension. The mapping ofindices in Z^n 
%	to Z cycles progressively through lower dimensional indices to higher: e.g. (i,j) -> i+(j-1)|i|.
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


set_of_vectors              = reshape(set_of_vectors, vector_dimensions, []);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Perform a change of coordinates using the input basis 
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


set_of_vectors              = basis * set_of_vectors;


