
function    [irradiances_P] = ...
			........................................................................................................................................................ 
            analysis_Solar_Irradiance___0___nM



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Stop Watch: Begin timing program                                                                                                                        %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Start program stop-watch.    
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


tic


%%&%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Import: model parameters and physical parameters, and the Planet-Star relative position vector.                                                         %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	<Description>     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


[dimensions_P, non_trivial_dimensions_P] = analyze_Planet_Partition_Dimensions___0___RS;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Import the structure containing physical and estimated constants: radii (km) of the Star and Planet, and the Star's luminosity (W). 
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


[radii, ~, ~, luminosity_S]              = parameters_Physical___0___2Sr2S;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Import the relative Planet-Star position vector (in km) at the desired day from SPICE data saved in the Excel file, then calculate its norm.    
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


[relative_position_PS, ~]                = import_Sun_Earth_Relative_Position_Velocity___St___2C;
relative_position_norm_PS                = norm(relative_position_PS);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Import the change of basis and origin transformations that represent the Star and Planet vectors with respect to a single coordinate system.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


[change_of_bases, origins]               = define_Coordinate_Transformations___0___2Sr(relative_position_norm_PS);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Generate unit normal vectors centered on the Star and Planet subsurfaces partitions. These are represented in Cartesian coordinates, in matrices whose first
%	dimension stores vector information and remaining dimensions index the partition (phi, theta). The output is a structure containing both matrices. 
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


partitions                               = generate_Partition_Unit_Normal_Vectors___0___Sr;


%%&%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Calculate: intermediate irradiance result (everything but the geometric projection onto the Star's surface and limb darkening).				            %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Identify the differences between all partitioned surface points on the Star and Planet, and the projections of these onto the normal vectors in these sets. 
%	The relevant results are all scalars output in arrays whose first dimensions correspond to the Star and latter to the Planet: {phi_S, theta_S, phi_P, theta_P}. 
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


[norms_SP, projections_SPxS, projections_PSxP] = norms_BA_Projections_ABxA_BAxB___2M3Sr___2M(partitions.unit_normal_vectors, change_of_bases, origins, radii);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Clear the partitions variable for efficiency.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


clear 											 partitions


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Calculate the surface area of each partitioned sub-surface on the Star (for use with output flux). By symmetry, this is only dependent on phi_S.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


partitioned_area_ratios_S                      = generate_Star_Area_Ratios___0___M;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Calculate the solar irradiance (incident on each partitioned Planet sub-surface from each partitioned Star sub-surface) intermediate result.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


irradiances_intermediate_P                     = luminosity_S .* partitioned_area_ratios_S .* norms_SP.^(-2) .* projections_PSxP;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Clear intermediate variables for efficiency.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


clear 									         luminosity_S	partitioned_area_ratios_S	norms_SP 	projections_PSxP


%%&%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Calculate: final solar radiance results                                                                                                                 %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Import limb darkening data and count the number of spectral bands.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


[spectral_bands, spectral_irradiances, spectral_coefficients] = import_Limb_Darkening_Data___0___2M1C;
number_of_spectral_bands                                      = size(spectral_bands,1);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Create a matrix to store spectral irradiance values for each partitioned Planet sub-surface.   
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


irradiances_P                                                 = zeros([dimensions_P number_of_spectral_bands]);


%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%
%%%%	For each spectral band:
%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%


for i = 1:number_of_spectral_bands


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	<Description>     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


	normalizing_constant           = (8*spectral_coefficients(i,4)+3*pi*spectral_coefficients(i,3)+12*spectral_coefficients(i,2)+6*pi*spectral_coefficients(i,1))/6;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Use one factor of the Star's projections to account for surface alignment, and another expanded in a power series to account for limb darkening.       
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


	limb_darkened_projections_SPxS = 1/normalizing_constant * projections_SPxS .* power_Series_Of_Matrix___MC___M (projections_SPxS, spectral_coefficients(i,:));


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Calculate the solar irradiance incident on each partitioned Planet sub-surface from each partitioned Star sub-surface.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


	spectral_irradiance_P          = spectral_irradiances(i,1) * limb_darkened_projections_SPxS .* irradiances_intermediate_P;
 

%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Sum solar irradiance contributions over the Star's partition (all non-trival {phi_S, theta_S} dimensions) to get total solar irradiance at each element of the
%	planet partition (indexed by {phi_P, theta_P}). Then remove any trivial dimensions left by default of the "sum" function, and convert irradiance units from 
%   km^-2 to m^-2.       
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


	spectral_irradiance_P          = sum(spectral_irradiance_P, [1 : ndims(spectral_irradiance_P)-non_trivial_dimensions_P]);
	spectral_irradiance_P          = squeeze(spectral_irradiance_P);
	spectral_irradiance_P          = spectral_irradiance_P * (10^(-3))^2;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	<Description>     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


	irradiances_P(:,:,i)           = spectral_irradiance_P;


%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%
%%%%	End iterations over spectral bands. 
%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%


end


%%&%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Stop Watch: End Timing Program                                                                                                                          %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	End program stop-watch.  
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


toc


%%&%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%