
function    [spectral_bands, spectral_irradiances, spectral_coefficients] = ...
			........................................................................................................................................................
            import_Limb_Darkening_Data___0___2M1C


%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Import the limb darkening coefficient data and the columns in which the different data is located.                                  					%&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Import the Excel file location of the limb darkening data and its corresponding locations within the file.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


[excel_file_location, range_data, columns_bands, column_irradiances, columns_coefficients] = location_and_ranges_Solar_Limb_Darkening_Excel_File___0___2St2V1S;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Import the limb darkening data.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


limb_darkening_data                                                                        = readmatrix(excel_file_location, 'Range', range_data);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Assign variables to data elements.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


spectral_bands                                                                             = limb_darkening_data(:,columns_bands);
spectral_irradiances                                                                       = limb_darkening_data(:,column_irradiances);
spectral_coefficients                                                                      = limb_darkening_data(:,columns_coefficients);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Convert spectral irradiance and coefficient values imported as NaN (i.e. no data entered in Excel file) to zero.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


spectral_irradiances(isnan(spectral_irradiances))                                          = 0;
spectral_coefficients(isnan(spectral_coefficients))                                        = 0;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Normalize (L1) the irradiances.   
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


spectral_irradiances(1,1)                                                                  = 1-sum(spectral_irradiances(2:end,1));


%%&%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%