
function    [<comma-separated list of output variables>] = ...
			........................................................................................................................................................ 
            analysis_Solar_Irradiance_Spherical_Coordinate_Partitions



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Input: model partition and time parameters                                                                                                              %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Input the parameters of the spherical coordinate partitions of the Earth: angle intervals (in degrees), numbers of subintervals to partition.    
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


earth___theta_bounds_lower_and_upper = -[-90,90];
earth___phi_bounds_lower_and_upper   = -[-90,90];


earth___n_theta                      = 8;
earth___n_phi                        = 8;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Input the parameters of the spherical coordinate partitions of the Sun: angle intervals (in degrees), numbers of subintervals to partition.    
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


sun___theta_bounds_lower_and_upper   = -[-90,90];
sun___phi_bounds_lower_and_upper     = -[-90,90];


sun___n_theta                        = 8;
sun___n_phi                          = 8;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Input the desired time in UTC     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


time_UTC                             = '2025-01-01T12:00:00';


%%&%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Import: the radii of the Earth and Sun (JPL, cited), the solar luminosity (cited), and the relative position fo the Earth and Sun (SPICE).              %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Import the estimated radii (km) of the Earth and the Sun, and solar luminosity (W).     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


[ ~ , sun___radius, ~ , earth___radius, ~ , sun___luminosity] = constants_Physical_And_Estimated___0___V;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Import the Earth-Sun relative position and velocity (in km, km/s), at the desired time, from SPICE. Then calculate the Earth-Sun distance.    
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


earth_sun___relative_position_and_velocity                    = import_SPICE_Earth_Position_And_Velocity___St___V (time_UTC);
earth_sun___relative_position                                 = earth_sun___relative_position_and_velocity(1:3,1);
earth_sun___distance                                          = norm(earth_sun___relative_position);


%%&%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Perform all normal vector dependent calculations and consolidate the results.                                                                           %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Generate unit spherical coordinate partitions of the Earth and Sun   
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


earth___partition_unit_normal_vectors      = partition_Unit_Sphere_Into_Unit_Normal_Vectors_Centered___VVSS___M ( ...
																					                              earth___theta_bounds_lower_and_upper, ...
																					                              earth___phi_bounds_lower_and_upper, ...
																					                              earth___n_theta, ... 
																					                              earth___n_phi ...
																	                                            );


sun___partition_unit_normal_vectors        = partition_Unit_Sphere_Into_Unit_Normal_Vectors_Centered___VVSS___M ( ...
																					                              sun___theta_bounds_lower_and_upper, ...
																					                              sun___phi_bounds_lower_and_upper, ...
																					                              sun___n_theta, ...
																					                              sun___n_phi ...
																	                                            );


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Flip the x-axis of one partition's unit normal vectors (so the partitions generated above represent hemispheres facing one another in the same coordinate 
%	system).    
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


sun___partition_unit_normal_vectors(:,:,1) = -1*sun___partition_unit_normal_vectors(:,:,1);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Take the linear projection (dot product) of all possible pairs of Earth and Sun partitioned unit normal vectors.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


earth_sun___partition_dot_productions      = dot_products_Of_All_3D_Vector_Pairs_In_AxB___MM___M( ...
																						          earth___partition_unit_normal_vectors, ...
																						          sun___partition_unit_normal_vectors ...
																						        ); 


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Compose the linear tranformation (projection) with the indicator function on the positive reals (to eliminate values for surfaces not facing one another).     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


earth_sun___partition_dot_productions      = max(0,earth_sun___partition_dot_productions);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Calculate the distances between all pairs of normal vectors in {earth_partition x sun_partition}.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


earth_sun___partition_distances            = distances_Between_All_3D_Vector_Pairs_In_AxB___MM___M( ...
																						            earth___radius * earth___partition_unit_normal_vectors, ...
                                                                                                    sun___radius * sun___partition_unit_normal_vectors , ...
                                                                                                    earth_sun___distance ...
																								  );


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Clear unconsolidated normal vector information.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


clear earth___partition_unit_normal_vectors sun___partition_unit_normal_vectors



%%&%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%