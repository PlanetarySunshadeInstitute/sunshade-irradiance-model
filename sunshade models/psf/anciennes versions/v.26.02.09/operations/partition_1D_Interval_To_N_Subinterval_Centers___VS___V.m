
function    [partition_subintervals_centers] = ...
			........................................................................................................................................................
            partition_1D_Interval_To_N_Subinterval_Centers___VS___V (bounds_lower_and_upper, n)



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Divide the input interval into n equal parts, then return a row vector containing the centers of each subinterval.   	                                %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Identify the input lower and upper bounds, and Calculate the magnitude of the resulting interval. 
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


bound_lower                    = bounds_lower_and_upper(1,1);
bound_upper                    = bounds_lower_and_upper(1,2);

interval_magnitude             = bound_upper - bound_lower;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Calculate the magnitude of the subintervals created when dividing the input interval into n equal parts.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


subinterval_magnitude          = interval_magnitude / n;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Create a vector containing the center of each subinterval.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


partition_subintervals_centers = (bound_lower + subinterval_magnitude / 2) : subinterval_magnitude : bound_upper;


