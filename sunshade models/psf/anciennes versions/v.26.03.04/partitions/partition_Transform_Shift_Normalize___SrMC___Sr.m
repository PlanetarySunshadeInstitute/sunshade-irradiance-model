
function    [partition_data] = ...
			........................................................................................................................................................
            partition_Transform_Shift_Normalize___SrMC___Sr (partition_data, change_of_basis, origin)



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Perform a change of basis on the position and normal vectors, relocate the origin of the position vectors, and normalize the normal vectors.            %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Transform the partition's position and normal vectors into the common coordinate system.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


partition_data.position       = change_of_basis * partition_data.position;
partition_data.normal_vectors = change_of_basis * partition_data.position;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Shift the origin of the partition's coordinate system to that of the common.    
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


partition_data.position       = partition_data.position + origin;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Normalize the partition's normal vectors (last two arguments: take the L2 norm along the 1st dimension).  
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


partition_data.normal_vectors = partition_data.normal_vectors ./ vecnorm(partition_data.normal_vectors,2,1);


