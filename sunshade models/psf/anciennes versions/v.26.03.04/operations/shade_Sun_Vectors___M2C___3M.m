            shade_Sun_Vectors___M2C___3M (vectors_H, vector_P, origin_S)


%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	<Description>                                                                                                                                           %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Calculate the relevant vectors for input into the quadratic equation.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


vectors_PH          = vectors_H - vector_P;
vector_PS           = origin_S - vector_P;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Identify the quadratic equation coefficients.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


a                   = vecnorms(vectors_PH).^2;
b                   = sum(2*vectors_PH .* vector_PS, 1);
c                   = norm(vector_ba)^2 - radius_S^2;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Identify quadratic equation solutions.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


t_1                 = (-b + (b.^2 - 4 .* a .* c).^(.5)) ./ 2*a; 
t_2                 = (-b - (b.^2 - 4 .* a .* c).^(.5)) ./ 2*a;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Set imaginary solutions to NaN.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


t_1(imag(t_1) ~= 0) = NaN;
t_2(imag(t_2) ~= 0) = NaN;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Take the maximum of both solutions (to choose positive solutions over negative), then set any negative solutions to zero.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


t_1                 = max(t_1,t_2);
t_1(t_1 <= 0)       = NaN;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	<Description>     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


vector_magnitudes_PH = vecnorm(vectors_PH, 2, 1);

vector_magnitudes_PS = t_1 * vector_magnitudes_PH;
unit_vectors_PS      = vectors_PH ./ vector_magnitudes_PH;
normal_vectors_S     = t_1 * vectors_PH - origin_S;


