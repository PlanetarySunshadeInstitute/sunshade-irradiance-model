
function    [planet_partition_dimensions, planet_partition_non_trivial_dimensions] = ...
			........................................................................................................................................................
            analyze_Planet_Partition_Dimensions___0___RS


%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Identify the total number of non-trival dimensions amongst the input dimension magnitudes (Matlab assigns trivial dimension when this magnitude is 1).  %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Import model parameters     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


model_parameters                            = input_Model_Parameters___0___Sr;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Identify the magnitudes of the input planet partition's dimensions    
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


planet_partition_dimensions                 = [model_parameters.planet.phi.n model_parameters.planet.theta.n];


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Start counting at zero.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


planet_partition_non_trivial_dimensions     = 0;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	If the magnitude of the first input dimension is greater than 1, Matlab will not assign the dimension trivally.   
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


if planet_partition_dimensions(1,1) > 1

	planet_partition_non_trivial_dimensions = planet_partition_non_trivial_dimensions + 1;

end


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	If the magnitude of the second input dimension is greater than 1, Matlab will not assign the dimension trivally.   
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


if planet_partition_dimensions(1,2) > 1

	planet_partition_non_trivial_dimensions = planet_partition_non_trivial_dimensions + 1;

end


