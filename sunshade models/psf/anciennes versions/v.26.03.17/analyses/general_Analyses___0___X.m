
function    [varargout] = ...
			........................................................................................................................................................ 
            general_Analyses___0___X 



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Perform the analyses according to the input model parameters.                                                                                           %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Start the program stopwatch.    
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


tic


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Import the input model parameters.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


model_parameters                      = import_Model_Parameters___0___Sr;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	<Description>     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


switch model_parameters.ouput.type


%%&%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Case: Matlab output                                                                                                                                     %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	<Description>     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


	case 'matlab'


%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%
%%%%	For each time period indicated in the input model parameters:
%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%


		for i = 1:model_parameters.time.periods


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Count the number of days past the start date at which to perform the current analysis iteration.    
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


			model_parameters.time.day_counter = (i-1) * model_parameters.time.frequency;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Perform the desired analysis at the corresponding date and store the results.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


			switch model_parameters.analysis_type

				case 'irradiance'
					
					results(:,:,:,i)          = analysis_Irradiance___Sr___nM(model_parameters.time);

				case 'shaded irradiance'
					
					results(:,:,:,i)          = analysis_Shaded_Irradiance___Sr___nM(model_parameters.time);


				case 'shading factor'                 

					results(:,:,:,i)		  = analysis_Shaded_Irradiance___Sr___nM(model_parameters.time) ./ analysis_Irradiance___Sr___nM(model_parameters.time);
					results(:,:,:,i)		  = min(1,results(:,:,:,i));

				case 'irradiance factor'

					results(:,:,:,i)		  = analysis_Shaded_Irradiance___Sr___nM(model_parameters.time) ./ analysis_Irradiance___Sr___nM(model_parameters.time);
					results(:,:,:,i)		  = min(1,results(:,:,:,i));
					results(:,:,:,i)		  = 1 - results(:,:,:,i);
					
			end 


%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%
%%%%	End iterations over time periods. 
%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%


		end


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Output the results to the Matlab command window.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


		varargout{1} = results;


%%&%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Case: NC file output                                                                                                                                    %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	<Description>     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


	case 'nc file'


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Identify the full locations of the NC template and export files.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


		nc_files = location_NC_Templates_And_Exports___0___Sr;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Copy and rename the template in the exports folder.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


		copyfile(nc_files.locations.templates.irradiance_factor, nc_files.locations.exports.current_file);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Set all irradiance factors to one.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


		ncwrite(nc_files.locations.exports.current_file, 'df', ones(288,192,365));
		ncwrite(nc_files.locations.exports.current_file, 'dfl', ones(288,192,364));


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Define the time start, frequency, and number of periods for a regular year.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


		model_parameters.time.start     = model_parameters.time.regular_year.start;
		model_parameters.time.frequency = model_parameters.time.regular_year.frequency;
		model_parameters.time.periods   = model_parameters.time.regular_year.periods;


%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%
%%%%	For each time period indicated in the input model parameters:
%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%


		for i = 1:model_parameters.time.periods

			regular_year_day=i
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Count the number of days past the start date at which to perform the current analysis iteration.    
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


			model_parameters.time.day_counter = (i-1) * model_parameters.time.frequency;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Perform the desired analysis at the corresponding date and store the results.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


			switch model_parameters.analysis_type

				case 'irradiance'
					
					results(:,:,i)          = analysis_Irradiance___Sr___nM(model_parameters.time);

				case 'shaded irradiance'
					
					results(:,:,i)          = analysis_Shaded_Irradiance___Sr___nM(model_parameters.time);


				case 'shading factor'                 

					results(:,:,i)		  = analysis_Shaded_Irradiance___Sr___nM(model_parameters.time) ./ analysis_Irradiance___Sr___nM(model_parameters.time);
					results(:,:,i)		  = min(1,results(:,:,i));

				case 'irradiance factor'

					results(:,:,i)		  = analysis_Shaded_Irradiance___Sr___nM(model_parameters.time) ./ analysis_Irradiance___Sr___nM(model_parameters.time);
					results(:,:,i)		  = min(1,results(:,:,i));
					results(:,:,i)		  = 1 - results(:,:,i);
					
			end


%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%
%%%%	End iterations over time periods. 
%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%


		end


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	<Description>     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


		ncwrite(nc_files.locations.exports.current_file, 'df', results(:,73:145,:),[1 1 1]);
		ncwrite(nc_files.locations.exports.current_file, 'df', results(:,1:72,:),[1 217 1]);
		clear results


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Define the time start, frequency, and number of periods for a regular year.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


		model_parameters.time.start     = model_parameters.time.leap_year.start;
		model_parameters.time.frequency = model_parameters.time.leap_year.frequency;
		model_parameters.time.periods   = model_parameters.time.leap_year.periods;


%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%
%%%%	For each time period indicated in the input model parameters:
%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%


		for i = 1:model_parameters.time.periods

			leap_year_day=i
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Count the number of days past the start date at which to perform the current analysis iteration.    
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


			model_parameters.time.day_counter = (i-1) * model_parameters.time.frequency;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Perform the desired analysis at the corresponding date and store the results.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


			switch model_parameters.analysis_type

				case 'irradiance'
					
					results(:,:,i)          = analysis_Irradiance___Sr___nM(model_parameters.time);

				case 'shaded irradiance'
					
					results(:,:,i)          = analysis_Shaded_Irradiance___Sr___nM(model_parameters.time);


				case 'shading factor'                 

					results(:,:,i)		  = analysis_Shaded_Irradiance___Sr___nM(model_parameters.time) ./ analysis_Irradiance___Sr___nM(model_parameters.time);
					results(:,:,i)		  = min(1,results(:,:,i));

				case 'irradiance factor'

					results(:,:,i)		  = analysis_Shaded_Irradiance___Sr___nM(model_parameters.time) ./ analysis_Irradiance___Sr___nM(model_parameters.time);
					results(:,:,i)		  = min(1,results(:,:,i));
					results(:,:,i)		  = 1 - results(:,:,i);
					
			end


%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%
%%%%	End iterations over time periods. 
%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%


		end


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	<Description>     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


		ncwrite(nc_files.locations.exports.current_file, 'dfl', results(:,73:145,:),[1 1 1]);
		ncwrite(nc_files.locations.exports.current_file, 'dfl', results(:,1:72,:),[1 217 1]);
		clear results


%%&%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	<Description>                                                                                                                                           %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	End output type cases.    
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


end


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	End the program stopwatch.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


toc


%%&%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%










