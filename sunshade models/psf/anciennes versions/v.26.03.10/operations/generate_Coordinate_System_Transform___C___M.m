
function    [coordinate_system_transform] = ...
			........................................................................................................................................................
            generate_Coordinate_System_Transform___C___M (input_vector)



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Generate matrix that transforms the input coordinate system to an orthonormal system genereated from the input vector.                                  %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Define the first vector as the normalized input vector (place this in row 3) 
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


coordinate_system_transform(:,3)     = input_vector/norm(input_vector);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Find a linearly independent second vector based on the values of the first vector's coordinates.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


if coordinate_system_transform(1,3) < 0.9

	coordinate_system_transform(:,2) = [1 ; 0 ; 0];

else

	coordinate_system_transform(:,2) = [0 ; 1 ; 0];

end


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Take the cross product of the two linearly independent vectors to find a second orthogonal vector, then normalize it.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


coordinate_system_transform(:,2)     = cross(coordinate_system_transform(:,3), coordinate_system_transform(:,2));
coordinate_system_transform(:,2)     = coordinate_system_transform(:,2)/norm(coordinate_system_transform(:,2));


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Take the cross product of the first two orthonormal vectors to product the third.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


coordinate_system_transform(:,1)     = cross(coordinate_system_transform(:,3), coordinate_system_transform(:,2));


