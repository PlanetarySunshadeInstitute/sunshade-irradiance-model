
function    [varargout] = ...
			........................................................................................................................................................ 
            general_Analyses___0___X 



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Perform the analyses according to the input model parameters.                                                                                           %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Start the program stopwatch.    
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


tic


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Import the input model parameters.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


model_parameters                      = input_Model_Parameters___0___Sr;


%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%
%%%%	For each time period indicated in the input model parameters:
%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%


for i = 1:model_parameters.time.periods


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Count the number of days past the start date at which to perform the current analysis iteration.    
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


	model_parameters.time.day_counter = (i-1) * model_parameters.time.frequency;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Perform the desired analysis at the corresponding date and store the results.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


	switch model_parameters.analysis_type

		case 'irradiance'
			
			results(:,:,:,i)          = analysis_Irradiance___Sr___nM(model_parameters.time);

		case 'shaded irradiance'
			
			results(:,:,:,i)          = analysis_Shaded_Irradiance___Sr___nM(model_parameters.time);


		case 'shading factor'                 

			results(:,:,:,i)		  = analysis_Shaded_Irradiance___Sr___nM(model_parameters.time) ./ analysis_Irradiance___Sr___nM(model_parameters.time);

	end 


%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%
%%%%	End iterations over time periods. 
%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%


end


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	End the program stopwatch.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


toc


%%&%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Output the analysis results according to the output type defined in the model parameters.                                                               %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%
%%%%	Evaluate the analysis output type (defined in model parameters), and output the data accordingly. 
%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%


switch model_parameters.ouput.type


%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%
%%%%	Case 'matlab': output the results of the analysis direclty in the Matlab command window.
%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%


	case 'matlab'

		varargout{1} = results;


%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%
%%%%	Case'preconfigured: low resolution': output an NC file corresponding to the low-resultion climate modeler requirements.
%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%


	case 'nc file'


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Define all zero results as NaN.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


		results(results==0) = nan;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Assign zero (NaN) results to the two dark longitudinal regions of the globe and append these to the calculated results.       
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


		dark_quarter_region = nan(size(results,1), 89);		
		results             = [dark_quarter_region results dark_quarter_region];


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Define the 'shading_percent' variable of the output structure as the results.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


		output_structure.shading_percent = results;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Define the latitude and longitude partitions as separate column vectors.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


		output_structure.lat = [-90:90]';
		output_structure.lon = [-180:179]';


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Output the results to the NC file defined in the model parameters.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


		nccreate(model_parameters.ouput.nc_file.file_location, 'shading_percent', 'Dimensions', {'lat', 181 ,'lon', 360});
		ncwrite(model_parameters.ouput.nc_file.file_location, 'shading_percent', output_structure.shading_percent);

		nccreate(model_parameters.ouput.nc_file.file_location, 'lat', 'Dimensions', {'lat', 181});
		ncwrite(model_parameters.ouput.nc_file.file_location, 'lat', output_structure.lat);
		nccreate(model_parameters.ouput.nc_file.file_location, 'lon', 'Dimensions', {'lon', 360});
		ncwrite(model_parameters.ouput.nc_file.file_location, 'lon', output_structure.lon);



%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%
%%%%	End output type cases. 
%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%


end


%%&%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%