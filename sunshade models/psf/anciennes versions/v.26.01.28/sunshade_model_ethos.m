% Multi-Object Penumbral Shading Simulation on Earth  (3-axis offsets + progress)
% Computes and visualizes cumulative penumbral shading (%) on Earth's
% surface due to many annular objects, reporting percent-complete status,
% and displays average sunlight reduction over the Earth disk.
%


%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Clear workspace and command window                                                                                                                      %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


clear; clc;


%%&%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Define architecture parameters and randomize their spatial distribution                                                                                 %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Define architecture (annulus) inner and outer radius (km), and number of structures   
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


structure_inner_radius       = 10;         
structure_outer_radius       = 20;         
number_of_structures         = 8;        


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Define structure distances (10^6 km) by random uniform distribution
%
%   Generic examples of other options
%
%		- Uniform linear:     track_func = @(N) linspace(track_min,track_max,N);
%		- Cosine U-shape:     track_func = @(N) track_min + (track_max-track_min)*((1-nthroot(cos(linspace(0,pi,N)),1))/2);
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


structure_distance_min       = 2.31;
structure_distance_max       = 2.41;
dist_func                    = @(N) structure_distance_min + (structure_distance_max-structure_distance_min).*rand(1,N);
structure_distances          = dist_func(number_of_structures);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	"Cross-track / "normal" offset (Δy, km)"    
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


off_min                      = -13000;
off_max                      = 13000;
offset_func                  = @(N) off_min + (off_max-off_min)*((1 - (nthroot(cos((linspace(0,pi,N)-0)),3))) / 2);
offset_km                    = offset_func(number_of_structures);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	"Down-track / "along-orbit" offset (Δx, km)"    
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


track_min                    = -6000;
track_max                    = 6000;
track_func                   = @(N) track_min + (track_max-track_min).*rand(1,N);
track_km                     = track_func(number_of_structures);


%%&%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Define physical constants and grid                                                                                                                      %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Define the sun and earth radii (km), and the astronomical unit (km)  
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


radius_sun         = 695700;
radius_earth       = 6371;
astronomical_unit  = 149597870;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Calculate the angular radius (rad) of the sun (as perceived from earth)    
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


angular_radius_sun = asin(radius_sun / astronomical_unit);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	"Ground grid large enough to capture full penumbra of furthest object"   
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


n                  = 1000;                                           % grid resolution
r_max_penumbra     = (min(structure_distances) * 1e6) * (angular_radius_sun + structure_outer_radius / (min(structure_distances) * 1e6));
r_max              = max(r_max_penumbra, radius_earth);
x                  = linspace(-r_max, r_max, n);
y                  = linspace(-r_max, r_max, n);
[X,Y]              = meshgrid(x,y);

shading_total      = zeros(n,n);
pctStep            = max(floor(number_of_structures/100),1);             % 1 % reporting step


%%&%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Define function: "OVERLAP AREA HELPER"                                                                                                                  %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


function A = overlap_area(R, r, d)

    if d >= (R + r)

        A     = 0;

    elseif d <= abs(R - r)

        A     = pi * min(R, r)^2;

    else

        alpha = acos((d.^2 + R.^2 - r.^2) ./ (2 .* d .* R));
        beta  = acos((d.^2 + r.^2 - R.^2) ./ (2 .* d .* r));
        A     = R.^2 .* alpha + r.^2 .* beta - 0.5 .* sqrt(( -d + R + r ) .* ( d + R - r ) .* ( d - R + r ) .* ( d + R + r ));

    end

end


%%&%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Display progress                                                                                                                                        %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


dq = parallel.pool.DataQueue;
afterEach(dq,@printPct);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Define function: Display progress  
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


function printPct(p)

    persistent last

	if isempty(last)

		last = 0

	end

    if p > last

		last = p

		fprintf('Progress: %d%% complete\n',p)

    end

end


%%&%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	"Mainloop (parfor)"                                                                                                                                     %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%
%%%%	For each architecture
%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%


parfor k = 1:number_of_structures


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Display progress     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


    if mod(k,pctStep)==0 || k==number_of_structures

		send(dq,round(100*k/number_of_structures))

	end


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	"Current object's parameters"   
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


    D_km                                           = structure_distances(k)*1e6;
    dy_km                                          = offset_km(k);
    dx_km                                          = track_km(k);

    Rout                                           = structure_outer_radius / D_km;          % outer annulus angular radius
    Rin                                            = structure_inner_radius / D_km;          % inner annulus angular radius


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	"Angular separation for each ground point (3-axis)"    
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


    Sep_ang                                        = sqrt( (X+dx_km).^2 + (Y+dy_km).^2 ) / D_km;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	"Overlap fractions"   
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


    over_out                                       = arrayfun(@(d) overlap_area(Rout,angular_radius_sun,d), Sep_ang);
    over_in                                        = arrayfun(@(d) overlap_area(Rin ,angular_radius_sun,d), Sep_ang);
    shade_i                                        = (over_out - over_in) ./ (pi*angular_radius_sun^2) * 100;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	"Variable opacity (inner=1, outer= inner/outer)" 
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


    t                                              = (Sep_ang - Rin) ./ (Rout - Rin);
    t                                              = min(max(t,0),1);
    opacity                                        = 1 - (1 - (structure_inner_radius/structure_outer_radius)) .* t;
    shade_i                                        = shade_i .* opacity;

    shade_i(Sep_ang > (Rout + angular_radius_sun)) = 0;  % outside penumbra
    shading_total                                  = shading_total + shade_i;    % accumulate


%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%
%%%%	End iterations over architectures
%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%


end


%%&%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	"Metrics and Visuals"                                                                                                                                   %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	<Description>     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


mask_earth    = (X.^2 + Y.^2) <= radius_earth^2;
avg_reduction = mean(shading_total(mask_earth),'all')


