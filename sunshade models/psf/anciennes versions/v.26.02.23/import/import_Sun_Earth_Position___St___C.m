
function    [position_SE_Data] = ...
			........................................................................................................................................................
            import_Sun_Earth_Position___St___C (time_UTC)


%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Import the sun-earth position vector associated with the input UTC date in the Excel and export it as a column vector.                                  %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Import the Excel file location of the sun-earth position vs. time data and the corresponding data locations within the file.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


[excel_File_Location, range_Time_Data, range_Position_Data] = positions_SE_Excel_File___0___3St;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Import the sun-earth position vs. time data.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


position_SE_Data                                            = readmatrix(positions_SE_Excel_File___0___3St,'Range',range_Position_Data);
time_UTC_Data                                               = readcell(positions_SE_Excel_File___0___3St,'Range',range_Time_Data);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Find the row corresponding to the input UTC date (represented by the first ten characters).     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


row_At_Input_Time_UTC                                       = find(strncmp(time_UTC_Data, time_UTC, 10));


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Collapse the sun-earth position data to the value at the input UTC date, and convert it to a 3D column vector.   
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


position_SE_Data                                            = position_SE_Data(row_At_Input_Time_UTC,:);
position_SE_Data                                            = position_SE_Data';


