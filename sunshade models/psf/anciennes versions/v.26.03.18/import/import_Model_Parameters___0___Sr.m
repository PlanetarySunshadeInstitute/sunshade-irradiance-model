
function    [model_parameters] = ...
			........................................................................................................................................................
            import_Model_Parameters___0___Sr



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Import the model parameters and apply any selected preconfigurations.                                                                                   %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Import the input model parameters.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


[model_parameters]                              = input_Model_Parameters___0___Sr;


%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%
%%%%	Apply any preconfigurations.
%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%


switch model_parameters.configuration


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Case 'preconfigured: low resolution': change the corresponding parameters to their preconfigured values.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


	case 'preconfigured: low resolution'

		model_parameters                        = preconfiguration_Partitions_Climate_Model_Low_Resolution___Sr___Sr (model_parameters);


%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%
%%%%	End preconfiguration cases. 
%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%


end


%%&%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Identify the dimensions (trivial and non trival) of the planet partition.                                                                               %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Identify the dimensions of the Planet partition.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


model_parameters.planet.dimensions.actual      = [model_parameters.planet.phi.n model_parameters.planet.theta.n];


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Identify the number of non-trivial dimensions associated with the planet partition (Matlab assigns trivial dimension when this magnitude is 1).     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


model_parameters.planet.dimensions.non_trivial = identify_Non_Trivial_Dimensions___R___S(model_parameters.planet.dimensions.actual);


%%&%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Calculate the effective area of a heliogyro unit.                                                                                                       %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Generate the polygon model of a heliogyro and calculate its area.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


polygon                                         = generate_Polygon_Vertices_R2x0___Sr___Sr(model_parameters.shade.heliogyro);
model_parameters.shade.heliogyro.area           = area(generate_Polygon_R2___Sr___P (polygon.R2x0).shape);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Incorporate the material absorption factor into the heliogyro area, and label the result as the shade unit effetive area.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


model_parameters.shade.heliogyro.area_effective = model_parameters.shade.heliogyro.material_irradiance_absorption * model_parameters.shade.heliogyro.area;


%%&%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


