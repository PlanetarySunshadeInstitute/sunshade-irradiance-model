            polygon_Intersection_Area___PP___S (polygon_A, polygon_B)



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Intersect the two input polygons (Matlab polyshapes) and calculate the area of their intersection.                                                      %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Intersect the two input polygons (polyshapes).     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


polygon_intersection      = intersect(polygon_A, polygon_B);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Calculate the area of their intersection.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


polygon_intersection_area = area(polygon_intersection);


