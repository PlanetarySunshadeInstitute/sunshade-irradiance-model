
function    [results] = ...
			........................................................................................................................................................
            analysis_Iterate_And_Switch___Sr2St___M (time, partitions, analysis_type, counter_display)



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Begin iterations over time periods.                                                                                                                     %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%
%%%%	For each time period indicated in the input model parameters:
%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%


for i = 1:time.periods


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	<Description>     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


	display(strcat(counter_display, int2str(i)))


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Count the number of days past the start date at which to perform the current analysis iteration.    
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


	time.day_counter = (i-1) * time.frequency;


%%&%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	If this is the first time period, perform the desired analysis and record information pertaining to the output's dimensions.                            %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%
%%%%	If this is the first time period being analyzed
%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%


	if i == 1 


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Perform the desired analysis at the corresponding start date.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


		switch analysis_type

			case 'irradiance'
				
				results_initial           = analysis_Irradiance___Sr___nM(time, partitions);

			case 'shaded irradiance'
				
				results_initial           = analysis_Shaded_Irradiance___Sr___nM(time, partitions);

			case 'shading factor'                 

				results_initial           = analysis_Shaded_Irradiance___Sr___nM(time, partitions) ./ analysis_Irradiance___Sr___nM(time, partitions);
				results_initial           = min(1,results_initial);

			case 'irradiance factor'

				results_initial           = analysis_Shaded_Irradiance___Sr___nM(time, partitions) ./ analysis_Irradiance___Sr___nM(time, partitions);
				results_initial           = min(1,results_initial);
				results_initial           = 1 - results_initial;
				
		end 


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Create a cell array of colons that can be used as indices to interate across all dimensions of an instantaneous result.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


		indices_intemporal                = repmat({':'}, 1, ndims(results_initial));


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Find the size of the instantaneous result array, and preallocate a general array to collect results for each time period.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


		dimensions_intemporal             = size(results_initial);
		results                           = zeros([dimensions_intemporal time.periods]);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Assign the initial results to the general array, then clear the initial results.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


		results(indices_intemporal{:}, i) = results_initial;
		clear                               results_initial;


%%&%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	If this isn't the first time period, perform the desired analysis on the new time period and append the results to the appropriate temporal dimension.  %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%
%%%%	If this isn't the first time period being analyzed
%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%


	else 


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Perform the desired analysis at the new time period and concatenate them along the temporal dimension of the existing results.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


		switch analysis_type

			case 'irradiance'
				
				results(indices_intemporal{:}, i) = analysis_Irradiance___Sr___nM(time, partitions);

			case 'shaded irradiance'
				
				results(indices_intemporal{:}, i) = analysis_Shaded_Irradiance___Sr___nM(time, partitions);

			case 'shading factor'                 

				results(indices_intemporal{:}, i) = analysis_Shaded_Irradiance___Sr___nM(time, partitions) ./ analysis_Irradiance___Sr___nM(time, partitions);
				results(indices_intemporal{:}, i) = min(1, results(indices_intemporal{:}, i));

			case 'irradiance factor'

				results(indices_intemporal{:}, i) = analysis_Shaded_Irradiance___Sr___nM(time, partitions) ./ analysis_Irradiance___Sr___nM(time, partitions);
				results(indices_intemporal{:}, i) = min(1, results(indices_intemporal{:}, i));
				results(indices_intemporal{:}, i) = 1 - results(indices_intemporal{:}, i);
				
		end 


%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%
%%%%	End conditional statement on time periods. 
%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%


	end


%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%
%%%%	End iterations over time periods.
%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%


end



%%&%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%