
function plot_irradiance_diagnostic___M___0 (results_2d, lat_deg, time_doy, save_path)


%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Plot a latitude-vs-time irradiance factor diagnostic chart.                                                                                             %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%   INPUTS
%     results_2d   [N_lat x N_time]   irradiance factor values (central longitude column, monthly samples)
%     lat_deg      [1 x N_lat]        latitude in degrees, e.g. linspace(-90, 90, N_lat)
%     time_doy     [1 x N_time]       day-of-year for each time column (1-indexed)
%     save_path    (optional string)  full path incl. filename for PNG output;
%                                     if omitted the figure is left open for inspection
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


if nargin < 4
	save_path = '';
end


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Month tick positions (day-of-year at the 1st of each month) and labels.
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


month_tick_doy = [1, 32, 60, 91, 121, 152, 182, 213, 244, 274, 305, 335];
month_labels   = {'Jan','Feb','Mar','Apr','May','Jun','Jul','Aug','Sep','Oct','Nov','Dec'};


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Wrap one column at each edge so the chart fills Jan-to-Dec without blank margins.
%   The Dec column is prepended at day (time_doy(end) - 365) and the Jan column is
%   appended at day (time_doy(1) + 365), then the axis is clipped to [1, 365].
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


time_ext = [time_doy(end) - 365,  time_doy(:)',  time_doy(1) + 365];
data_ext = [results_2d(:, end),   results_2d,    results_2d(:, 1)  ];


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Build the 2-D mesh for contourf.
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


[T, L] = meshgrid(time_ext, lat_deg(:)');


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Colormap: red (0, no shading) -> white (mid) -> blue (max shading).
%   Matches the S&M convention where cooling/shading reads as blue.
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


n_colors = 256;
frac     = linspace(0, 1, n_colors)';
r_ch     = interp1([0, 1], [0.10, 1.00], frac);
g_ch     = interp1([0, 1], [0.30, 1.00], frac);
b_ch     = interp1([0, 1], [0.80, 1.00], frac);
cmap     = [r_ch, g_ch, b_ch];


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Contour level range.
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


v_min = min(results_2d(:));
v_max = max(results_2d(:));
if abs(v_max - v_min) < 1e-6
	v_max = v_min + 0.01;   % guard for flat/all-zero input
end

n_levels = 16;
levels   = linspace(v_min, v_max, n_levels + 1);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Create figure and axes.
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


fig = figure ( ...
	'Name',        'Irradiance Factor — Latitude vs Month', ...
	'NumberTitle', 'off', ...
	'Color',       'white', ...
	'Position',    [100, 100, 680, 480] );

ax = axes(fig);
hold(ax, 'on');


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Draw filled contours with thin visible lines (matching S&M style).
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


[~, h_fill] = contourf(ax, T, L, data_ext, levels);
set(h_fill, 'LineColor', [0.35 0.35 0.35], 'LineWidth', 0.4);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Apply colormap and colorbar.
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


colormap(ax, cmap);
clim(ax, [v_min, v_max]);

cb              = colorbar(ax, 'eastoutside');
cb.Label.String = 'Irradiance factor';
cb.Label.FontSize = 10;
cb.FontSize       = 9;
cb.TickLabelsMode = 'manual';
cb.TickLabels     = arrayfun(@(v) sprintf('%.4f', v), cb.Ticks, 'UniformOutput', false);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Axis labels, limits, and ticks.
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


ylabel(ax, 'Latitude [deg]', 'FontSize', 11);
title(ax,  'Irradiance factor — latitude vs month', 'FontSize', 12, 'FontWeight', 'bold');

xlim(ax, [1, 365]);
ylim(ax, [-90, 90]);

yticks(ax,      [-90, -60, -30, 0, 30, 60, 90]);
yticklabels(ax, {'-90°', '-60°', '-30°', '0°', '30°', '60°', '90°'});

xticks(ax,      month_tick_doy);
xticklabels(ax, month_labels);

set(ax, 'TickDir', 'out', 'Box', 'on', 'FontSize', 10, 'Layer', 'top', ...
        'XGrid', 'off', 'YGrid', 'off');

hold(ax, 'off');
drawnow;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Save to PNG (preconfigured mode) or leave open for inspection (manual mode).
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


if ~isempty(save_path)
	figure(fig);   % bring to front and ensure it is the current figure
	drawnow;
	try
		exportgraphics(fig, save_path, 'Resolution', 200);
	catch
		try
			saveas(fig, save_path, 'png');
		catch err
			warning('Could not save diagnostic chart: %s\n  %s', save_path, err.message);
		end
	end
	fprintf('Saved diagnostic chart: %s\n', save_path);
	close(fig);
end


