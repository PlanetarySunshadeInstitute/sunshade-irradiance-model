
function    [angular_velocity_norm] = ...
			........................................................................................................................................................
            angular_Velocity_Norm___2C___S (vector_to_center_of_rotation, vector_linear_velocity)



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Calculate the angular velocity norm based on the input vector (from the object to its center of rotation) and its linear velocity.                      %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Calculate angular velocity using the input vectors and the formula |rxv|/r^2.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


angular_velocity      = cross(vector_to_center_of_rotation,vector_linear_velocity) / norm(vector_to_center_of_rotation)^2;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Calculate the norm of the angular velocity vector.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


angular_velocity_norm = norm(angular_velocity);


