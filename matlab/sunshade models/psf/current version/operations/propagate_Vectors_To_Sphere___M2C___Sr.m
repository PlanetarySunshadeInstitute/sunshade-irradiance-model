
function    [vectors] = ...
			........................................................................................................................................................
            propagate_Vectors_To_Sphere___M2C___Sr (vector_set_H, vector_P, center_S, radius_S)


%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	<Description>                                                                                                                                           %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Calculate the differences between the vectors in set H and vector p, as well as the difference between vector P and the center of the sphere.       
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


vectors.p.H                     = vector_set_H - vector_P;
vectors.p.s                     = vector_P - center_S; 


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	For each vector in the set pxH, calculate the quadratic equation coefficients that will solve for the scalar that will propagate it to the sphere surface.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


a                               = vecnorm(vectors.p.H).^2;
b                               = sum(2*vectors.p.H .* vectors.p.s, 1);
c                               = norm(vectors.p.s)^2 - radius_S^2;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Calculate the two solutions to the quadratic equation.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


t_1                             = (-b + (b.^2 - 4 .* a .* c).^(.5)) ./ (2*a);
t_2                             = (-b - (b.^2 - 4 .* a .* c).^(.5)) ./ (2*a);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Set imaginary and negative solutions to NaN.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


t_1(imag(t_1) ~= 0 | t_1 <= 0 ) = NaN;
t_2(imag(t_2) ~= 0 | t_2 <= 0 ) = NaN;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Take the minimim of both positive solutions (to avoid solutions that intersect the sun twice). 
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


t_1                             = min(t_1,t_2);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Identify columns corresponding to vectors with no solutions, then reduce vector sets to just these columns.       
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


vectors.that_intersect_sphere   = ~isnan(t_1);

vectors.p.H                     = vectors.p.H(:, vectors.that_intersect_sphere);
t_1                             = t_1(:,vectors.that_intersect_sphere);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Calculate the norms of the differences between the vectors in the set H and the vectors p, as well as the unit vectors in these directions.    
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


vectors.norms.p.H               = vecnorm(vectors.p.H, 2, 1);
vectors.unit.p.S                = vectors.p.H ./ vectors.norms.p.H;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Calculate the norms of the vectors that extend pxH to the sphere surface.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


vectors.norms.p.S               = t_1 .* vectors.norms.p.H;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Calculate the unit normal vectors on the sphere at each point of intersection with the extensions of vectors pxH.
%	The intersection point in the common coordinate system is P + t_1*(H - P) = t_1*vectors.p.H + vector_P.
%	The outward normal at that point is (intersection - center_S) = t_1*vectors.p.H + vectors.p.s,
%	where vectors.p.s = vector_P - center_S (computed above). The previous expression "t_1.*vectors.p.H - center_S"
%	omitted the vector_P offset, producing a small (~0.06-0.14 degree) angular error in the normal direction.
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


vectors.unit_normal.S           = t_1 .* vectors.p.H + vectors.p.s;
vectors.unit_normal.S           = vectors.unit_normal.S ./ vecnorm(vectors.unit_normal.S, 2, 1);


%%&%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%