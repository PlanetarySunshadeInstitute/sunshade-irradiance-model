
function    [model_parameters] = ...
			........................................................................................................................................................ 
            input_Model_Parameters___0___Sr 



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Input the desired analysis type.                                                                                                                        %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Input the desired analysis type: 'irradiance', 'shaded irradiance', 'shading factor', or 'irradiance factor'.    
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


model_parameters.analysis_type               = 'irradiance factor';


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Input the desired partition configuration type:
%
%       'manual'                       — user-defined partition parameters below; results output to command window.
%
%       'preconfigured: low resolution' — CESM f09 grid (192 lat x 288 lon). Computes irradiance factor at the
%                                         central longitude strip only (theta.n = 1) and replicates it across all
%                                         288 CESM longitude columns. Fast. Correct for rotationally symmetric
%                                         constellations when only the latitude profile is needed.
%
%       'preconfigured: full resolution' — CESM f09 grid (192 lat x 288 lon). Computes irradiance factor across
%                                          all 144 disc longitude columns (the full sun-facing hemisphere), so each
%                                          CESM longitude bin receives its physically correct value based on its
%                                          angular distance from the sub-stellar point. The sub-stellar point is
%                                          placed at CESM longitude 0°; the 144 nightside columns are set to 1.0.
%                                          ~144x slower than low resolution. Use when true longitude variation
%                                          in the output file is required.
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


model_parameters.configuration               = 'preconfigured: full resolution';


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Input the desired analysis output type: 'matlab', 'nc file' [Note: Currently this has no effect]
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


%model_parameters.ouput.type                  = 'nc file';


%%&%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Input the desired start time, frequency, and number periods over which the analysis is to be performed.                                                 %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Input the desired start time in UTC     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


model_parameters.time.start            = '2024-01-01T00:00:00';


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Input the desired frequency (an integer number of days N): one analysis will be performed every N days.    
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


model_parameters.time.frequency        = 1;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Input the desired number of periods over which the analysis is to be performed.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


model_parameters.time.periods          = 1;


%%&%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Input: model partition parameters (if a preconfiguration is selected, these will be overwritten upon import).                                           %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Input the parameters of the surface partitions of the Star: angle intervals (in degrees), numbers of subintervals to partition.    
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


		model_parameters.star.phi.interval     = [-90,90];
		model_parameters.star.theta.interval   = [-90,90];

		model_parameters.star.phi.n            = 11;
		model_parameters.star.theta.n          = 11;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%		Input the parameters of the surface partitions of the Planet: angle intervals (in degrees), numbers of subintervals to partition.    
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


		model_parameters.planet.phi.interval   = [-90,90];
		model_parameters.planet.theta.interval = [-90,90];

		model_parameters.planet.phi.n          = 11;
		model_parameters.planet.theta.n        = 11;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Input strings indicating the type of Star and Planet surface partitions {'sphere', 'disc'}, and that of the Shade {'heliogyros'}.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


		model_parameters.star.partition_type   = 'sphere';
		model_parameters.planet.partition_type = 'disc';
		model_parameters.shade.partition_type  = 'heliogyros';


%%&%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Input design and material properties of an individual heliogyro unit.                                                                                   %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Input the number of blades (the number of partitions of the angle 2pi, on the inner and outer surfaces of the heliogyro). 
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


model_parameters.shade.heliogyro.sides.inner                    = 52;
model_parameters.shade.heliogyro.sides.outer                    = 52;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Input the inner and outer radius (in km) of each blade.    
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


model_parameters.shade.heliogyro.radii.inner                    = 10;
model_parameters.shade.heliogyro.radii.outer                    = 20;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Input the material irradiance absorption constant: kappa in [0,1].    
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


model_parameters.shade.heliogyro.material_irradiance_absorption = .6535; 


%%&%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%