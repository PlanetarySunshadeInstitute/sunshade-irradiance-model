            transform_R2x0_To_R3_To_R2___M___M (R2x0, R3)



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Transform the input R2 coordinate system to a plane normal to an R3 vector with origin at an R3 point, then project to the plane normal to the R3 axis. %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	 Create a matrix to transform the coordinate system, such that coordinates in the XY plane would now lie in the plane normal to the input vector in XYZ.      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


transform_coordinates_matrix = generate_Coordinate_System_Transform___C___M(R3.normal);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Create a matrix that will change the basis of the coordinates into an orthonormal basis normal to the input axis.   
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


change_of_basis_matrix       = generate_Change_Of_Basis___C___M(R3.trajectory);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Perform the composition of these transformations on the input coordinates, and change the basis of the center point.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


R2x0                          = change_of_basis_matrix * transform_coordinates_matrix * R2x0;
R3.centerpoint                = change_of_basis_matrix * R3.centerpoint;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Shift the origin of the input coordinates to the input center point.       
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


R2x0                          = R2x0 + R3.centerpoint;


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Delete coordinates corresponding to the direction of the input axis, thereby collapsing the coordinates to an R2 coordinate system of the normal plane.    
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


R2                            = R2x0(1:2,:); 


