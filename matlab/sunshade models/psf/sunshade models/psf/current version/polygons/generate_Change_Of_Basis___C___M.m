function    [change_of_basis_matrix] = ...
			........................................................................................................................................................
            generate_Change_Of_Basis___C___M (input_vector)



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Generate a change of basis matrix to transform the input coordinate system to an orthonormal basis genereated from the input vector.                    %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Define the first vector as the normalized input vector (place this in row 3) 
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


change_of_basis_matrix(3,:)     = input_vector'/norm(input_vector);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Find a linearly independent second vector based on the values of the first vector's coordinates.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


if change_of_basis_matrix(3,1) < 0.9

	change_of_basis_matrix(2,:) = [1 0 0];

else

	change_of_basis_matrix(2,:) = [0 1 0];

end


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Take the cross product of the two linearly independent vectors to find a second orthogonal vector, then normalize it.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


change_of_basis_matrix(2,:)     = cross(change_of_basis_matrix(3,:),change_of_basis_matrix(2,:));
change_of_basis_matrix(2,:)     = change_of_basis_matrix(2,:)/norm(change_of_basis_matrix(2,:));


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Take the cross product of the first two orthonormal vectors to product the third.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


change_of_basis_matrix(1,:)     = cross(change_of_basis_matrix(3,:),change_of_basis_matrix(2,:));


