
function    [vector_pairs] = ...
			........................................................................................................................................................
            identify_Vector_Pairs___M___M (vector_set)



%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Identify vector pairs of interest and output a matrix containing their (row,column) coordinates.                                                        %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Normalize each vector in the set.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


vector_set      = vector_set ./ vecnorm(vector_set, 2, 1);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Take the dot product of each unique combination.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


dot_products    = tril(vector_set' * vector_set, -1);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Identify the (row,column) indices of the vectors of interest.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


[rows, columns] = find(dot_products > 0.999999998572720);


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Combine the row and column indices into a single matrix, then sort it by the row indices.    
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


vector_pairs    = [rows columns];
vector_pairs    = sortrows(vector_pairs);


