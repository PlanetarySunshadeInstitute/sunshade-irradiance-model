
function    [series_partial_sum] = ...
			........................................................................................................................................................
            power_Series_Of_Matrix___MC___M (matrix, series_coefficients)


%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%	Calculate the sum of the power series of the input matrix with respect ot the input series coefficients.                                                %&%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Identify the desired order of the power series.    
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


series_order           = length(series_coefficients(1,:));


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Originally define the partial sum of the series as zero, and the power of the matrix as unity.   
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


series_partial_sum     = zeros(size(matrix));
matrix_power           = ones(size(matrix));


%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%
%%%%	For each power series coefficient
%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%


for i = 1:series_order


%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	If the power of the current term in the series is greater than 1, multiply the previous matrix power by the input matrix to create it (for efficiency).      
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


	if i > 1

		matrix_power   = matrix_power .* matrix;

	end

%------------------------------------------------------------------------------------------------------------------------------------------------------------------%
%	Multiply the current series coefficient by its corresponding power of the matrix, and add it to the existing partial sum of the series.     
%------------------------------------------------------------------------------------------------------------------------------------------------------------------%


	series_partial_sum = series_partial_sum + series_coefficients(1,i) * matrix_power;


%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%
%%%%	End iterations over power series coefficients
%%%%::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::::%%%%


end


